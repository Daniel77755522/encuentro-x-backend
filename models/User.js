const mongoose = require('mongoose');
const bcrypt = require('bcryptjs');

const userSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true
    },
    password: {
        type: String,
        required: true
    },
    // --- NUEVO CAMPO PARA USUARIOS BLOQUEADOS ---
    blockedUsers: [{ // Array de IDs de usuarios que este usuario ha bloqueado
        type: mongoose.Schema.Types.ObjectId, // Tipo: es un ID de otro documento de usuario
        ref: 'User' // Referencia al modelo 'User' (para poder hacer .populate si es necesario)
    }],
    // --- FIN NUEVO CAMPO ---
    createdAt: {
        type: Date,
        default: Date.now
    }
});

userSchema.pre('save', async function(next) {
    if (!this.isModified('password')) {
        return next();
    }
    try {
        const salt = await bcrypt.genSalt(10);
        this.password = await bcrypt.hash(this.password, salt);
        next();
    } catch (error) {
        next(error);
    }
});

userSchema.methods.matchPassword = async function(enteredPassword) {
    return await bcrypt.compare(enteredPassword, this.password);
};

const User = mongoose.model('User', userSchema);

module.exports = User;